<br><br><b>Stocks :-</b>
Buying shares of a company actually means owning a small portion of it.
A company may declare various classes of shares, each of them having distinct value, perks and ownership rules.
Proper ownership of shares of a company is documented by issuance of stock certificate.
Stock Certificate is a document which specifies the amount of shares owned by an individual, type of shares and their value.

<br><br><b>Stocks exchange :-</b>
This is the place where actual trade of shares takes place. Investors invest their money in companies via stock exchange.
Listing
Thousands of companies get themselves listed at the stock exchange, so that more and more people invest in their company (by buying their shares) . These companies are called Public Companies as their shares are owned by common public.
These companies pay an amount to the stock exchange as a fee to get themselves listed, along with all the necessary information.

<br><br><b>Brokers :-</b>
In the share market,Brokers get companies listed and connect them with the investors.
Since there is large number of investors (in millions), stock exchange cannot deal with each of them separately, so they assign this task to the brokers. In other words, shares broker is an intermediate between you and the stock exchange.

