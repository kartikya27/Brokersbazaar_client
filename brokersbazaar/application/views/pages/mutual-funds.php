                            <p>Mutual funds are another means for people to invest, generally with a lesser risk and lesser returns too.
<br><br>But how are they different from stock market?
<br><br>Here's how:
<br><br>People from diverse backgrounds come and invest a part of their earnings in mutual funds, which in turn, results in a large capital in the hands of these Mutual Fund companies.
<br><br>These companies have a group of people, referred to as 'Fund Managers', who are having a great experience and sound understanding of how markets behave. The Fund Managers then invest the colossal capital from mutual funds into shares and overall financial market.

<br><br>The areas where Mutual fund companies invest are very diverse, could be anything - stocks, gold, real estate, oil etc.

<br><br>Some different types of mutual funds based on the duration of investment are
<br><br><b>i) Open End Mutual Funds:- </b>
<br>In these funds, anyone can walk in anytime and invest. Same rule for withdrawal of money,i.e.,one can get in or get out anytime as per his/her wish, there's no specified period until which investor cannot withdraw their investment.

<br><br><b>ii) Close End Mutual Funds:- </b>
<br>In these Mutual funds, there is a proper entry and exit time, a set duration before which investors can't withdraw their money.

<br><br>Now based on the areas where Mutual funds invest, some general types are :

<br><br><b>i) Money Market Funds:- </b>
<br>Short term investmentd are made in fixed income securities, such as government bonds, treasury bill etc. Investments are safe in nature but give lower returns


<br><br><b>ii) Equity Funds:- </b>
<br>When a major portion of the capital is invested in shares, funds are called Equity Funds.

<br><br><b>iii) Debt Funds:- </b>
<br>When most of the investments are made in safe assets such as Government bonds, company bonds, company papers etc, funds are called Debt funds.

<br><br><b>iv) Balanced Funds:- </b>
<br>These funds invest in both Equity funds and Debt funds in a balanced ratio, most probably 50:50 or 60:40.

<br><br><b>v) Speciality Funds:- </b>
<br>Speciality funds invest particularly in some specific field, for example,socially responsible funds are likely to invest in products which help a person to get over tobacco addiction, or drug addiction and aren't likely to invest in companies manufacturing alcohol or cigarettes.


<br><br>On the basis of companies in which mutual funds invest, there are:
<br><br><b>a) Large Cap Mutual funds:- </b>
<br>Large cap mutual funds invest a major portion of their capital in large companies. In this case, there are lesser chances of a loss as these big companies have a market for the services they provide.

<br><br><b>b) Mid cap Mutual funds:- </b>
<br>Mid cap mutual funds invest mainly in companies with medium capital. Here, there are chances for greater profits as a medium company can grow more in comparison with large companies, but at the same time, chances for the loss are also higher.

<br><br><b>iii) Small Cap Mutual funds:- </b>
<br>Small cap Mutual funds invest in small companies. Here, there are possibilities for greatest profits and losses.

<br><br><b>Benefits of Mutual Funds:- </b>

<br><br><b>i) Lesser Risk:- </b>
<br>If all the money is invested in buying shares of a company, say ABC,then any major blow to ABC will cause whole investment to sink.
<br><br>On the other hand, if we invest in different areas, loss to one company can be compensated by the other and hence loss can be minimised. Mutual Funds follow this strategy and hence are safer.

<br><br><b>ii) Lesser Complications:- </b>
<br>Mutual Funds are simpler to understand in comparison with stocks, so if anyone who is not having sound knowledge about share market can start with mutual funds for better returns</p>
