                            <h3>Insurance</h3>
                            <p>The term ' Insurance' exists because of the word 'Risk'.
<br><br>Risk basically results from uncertainty. For example, if we are certain that we will suffer a loss if we do one particular thing, we will obviously not do it, hence there is no risk.
<br><br>Risk comes into existence when there's uncertainty, and to minimise this risk of loss, we get our stuff insured.
<br><br>Insurance is a form of risk management, or in other words, it is ' equitable transfer of risk of loss from one party (the insured) to the other (insurer) in exchange of payment, mostly periodic in nature (called premium).

<br><br>How Insurance companies work?
<br><br>A question arises, why would someone pay for our loss in return for payment of a premium much smaller than the losses?
<br><br>The answer is pretty simple. An insurance company insures a large number of customers with similar insurable interest ( the one which is insured), for which all the customers pay regular premium.
<br><br>Now, not all the customers encounter a loss to their insured good(s) at the same time or during the period of insurance. Therefore, the insurance companies invest the money pooled and pay for the claims, and also earn profits.

<br><br><b>TYPES OF INSURANCE</b>
<br><br>Any risk that can be quantified or in other words, expressed in terms of money can be insured.
<br><br>Some common types of insurance are:
<br><br><b>i) Life insurance</b>
<br><br>Now one might argue how we can evaluate a person's life in terms of money. Well, we don't. We just evaluate the premium group s/he belongs, keeping in mind the health, age, financial status so that a certain sum of money is paid after a decided period of time or demise.
<br><br><b>ii) Automobile Insurance</b>
<br><br><b>iii) Property Insurance</b>
<br><br><b>iv) Health Insurance</b>
<br><br><b>v) Income protection Insurance</b>

<br><br>Now it is not this way that if an insured good(s) / entity faces a loss under any circumstances, then Insurance companies are liable to pay the compensation.
<br><br>Insurance companies mention the the conditions and the amount of compensation for loss under various circumstances, which must be taken care of while buying an Insurance.

<br><br><b>BENEFITS OF INSURANCE</b>
<br><br><b>Mainly, these are the reasons why we should buy insurance policies.</b>

<br><br>i) Transfer of risk, as in case of life insurance, if the insured dies or becomes disabled, then the sum assured is given so that the family expenses are met. Similarly, in health insurance, we are entitled to get the expenditure on treatment of any illness upto sum assured.</b>
<br><br>ii) Tax benefits are given by the government on buying insurance policies.

<br><br><b>DOs and DON'Ts</b>
<br><br>While buying a policy, keep these points in mind:
<br><br><b>DOs</b>
<br><br>1) Always fill out your insurance form by yourself.
<br><br>2)Make sure that your insurance company is registered with IRDA (Insurance Regulatory and Development Authority). At present, there are 24 Insurance companies listed in IRDA, cross examine from their official site.
<br><br>3)Make sure you buy your policy through a licensed agent/broker.
<br><br>4)Most importantly, read the insurance form thoroughly. Go through all the term and conditions, ask questions from your agent about what policy covers and what not, and buy only when completely satisfied with the services offered.
<br><br>4) Be very honest while filling the insurance form, any misinformation might render you ineligible for compensation.
<br><br>5) Ask your insurer/intermediary to explain the basis for deciding the sum assured.

<br><br><b>DON'Ts</b>
<br><br>1) Don't allow anyone else to fill your form.
<br><br>2)Don't leave any column blank, also, never sign an unfilled form.
<br><br>3) Don't conceal / mistake any facts about existing medical condition (while buying health insurance) or about the property to be insured.
<br><br>4) Don't mention the incorrect value of your property, concealing any facts might cause disputes at the time of claims.
<br><br>Also,visit official website of IRDA to get more knowledge before buying any insurance.

